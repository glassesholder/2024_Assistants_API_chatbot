"""
사용 가이드 페이지 모듈
"""
import streamlit as st

def render_guide_page():
    """사용 가이드 페이지 렌더링"""
    st.title("📚 Assistants API 챗봇 사용 가이드")
    
    st.markdown("""
    ### 🎯 사용 방법
    
    #### 1️⃣ **API Key 설정**
    - 좌측 사이드바에서 **"API Key 설정"**을 클릭합니다.
    - OpenAI API 키를 입력합니다.
    - API 키는 세션 동안 안전하게 보관됩니다.
    
    #### 2️⃣ **Assistant 생성**
    - **"Assistant 생성"** 메뉴로 이동합니다.
    - PDF/DOCS 파일을 업로드합니다.
    - Vector Store 이름을 지정합니다.
    - Assistant 이름과 설명을 입력합니다.
    - **"Assistant 생성"** 버튼을 클릭합니다.
    
    #### 3️⃣ **채팅하기**
    - Assistant 생성 완료 후 **"채팅"** 메뉴로 이동합니다.
    - 문서 기반의 질문을 입력합니다.
    - 실시간 스트리밍으로 답변을 받아볼 수 있습니다.
    
    #### 4️⃣ **정보 확인**
    - **"정보 보기"** 메뉴에서 생성된 리소스 상태를 확인할 수 있습니다.
    - Assistant, Vector Store, 업로드된 파일 정보를 볼 수 있습니다.
    
    #### 5️⃣ **리소스 정리**
    - **"대화 종료"** 메뉴에서 모든 리소스를 정리할 수 있습니다.
    - OpenAI 플랫폼의 Assistant, Vector Store, 파일들이 완전히 삭제됩니다.
    - 정리 후 자동으로 가이드 페이지로 돌아갑니다.
    
    ### 💡 주요 기능
    - **📁 다중 파일 업로드**: 여러 PDF 파일을 한 번에 업로드 가능
    - **🔄 실시간 스트리밍**: 답변을 실시간으로 확인
    - **🗂️ 리소스 관리**: 생성된 모든 리소스를 체계적으로 관리
    - **🧹 완전 정리**: OpenAI 플랫폼의 모든 리소스 완전 삭제
    
    ### ⚠️ 주의사항
    - 페이지를 새로고침하면 세션 정보가 초기화됩니다.
    - Assistant를 먼저 생성해야 채팅이 가능합니다.
    - 리소스 정리는 되돌릴 수 없으니 신중하게 사용하세요.
    - API 사용량에 따라 OpenAI 요금이 발생할 수 있습니다.
    
    ### 🚀 시작하기
    좌측 사이드바에서 **"API Key 설정"**부터 시작하세요!
    """)
